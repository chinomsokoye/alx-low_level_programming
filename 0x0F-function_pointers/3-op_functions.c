#include "3-calc.h"

/**
 * op_add - sum two integers
 * @a: first integer
 * @b: second integer
 * Return: sum of integers
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: integer
 * @b: integer
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two integers
 * @a: integer a
 * @b: integer b
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of two integers
 * @a: integer
 * @b: integer
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus of two integers
 * @a: integer
 * @b: integer
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
